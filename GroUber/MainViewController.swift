    func set_profile_image() {
        var image = loadImageFromDiskWith(fileName: "user_profile")
            if image != nil {
            image = ResizeImage(image: image!, targetSize: CGSize(width: 25, height: 25))
            image = image?.roundedImage
            
            let myTabBarItem3 = (self.tabBar.items?[2])! as UITabBarItem
            myTabBarItem3.image = image?.withRenderingMode(UIImage.RenderingMode.alwaysOriginal)
            myTabBarItem3.selectedImage = image?.withRenderingMode(UIImage.RenderingMode.alwaysOriginal)
            myTabBarItem3.title = "Profile"
            myTabBarItem3.imageInsets = UIEdgeInsets(top: 3, left: 0, bottom: -3, right: 0)
        }
    }
    
    
    func ResizeImage(image: UIImage, targetSize: CGSize) -> UIImage {
        let size = image.size
        
        let widthRatio  = targetSize.width  / image.size.width
        let heightRatio = targetSize.height / image.size.height
        
        // Figure out what our orientation is, and use that to form the rectangle
        var newSize: CGSize
        if(widthRatio > heightRatio) {
            newSize = CGSize(width: size.width * heightRatio, height: size.height * heightRatio)
        } else {
            newSize = CGSize(width: size.width * widthRatio,  height: size.height * widthRatio)
        }
        
        // This is the rect that we've calculated out and this is what is actually used below
        let rect = CGRect(x: 0, y: 0, width: newSize.width, height: newSize.height)
        
        // Actually do the resizing to the rect using the ImageContext stuff
        UIGraphicsBeginImageContextWithOptions(newSize, false, 1.0)
        image.draw(in: rect)
        let newImage = UIGraphicsGetImageFromCurrentImageContext()
        UIGraphicsEndImageContext()
        
        return newImage!
    }
    
    //load the profile image
    func loadImageFromDiskWith(fileName: String) -> UIImage? {
        
        let documentDirectory = FileManager.SearchPathDirectory.documentDirectory
        
        let userDomainMask = FileManager.SearchPathDomainMask.userDomainMask
        let paths = NSSearchPathForDirectoriesInDomains(documentDirectory, userDomainMask, true)
        
        if let dirPath = paths.first {
            let imageUrl = URL(fileURLWithPath: dirPath).appendingPathComponent(fileName)
            let image = UIImage(contentsOfFile: imageUrl.path)
            return image
            
        }
        
        return nil
    }
    
